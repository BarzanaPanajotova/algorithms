package com.bdpanajoto.algorithms.sorting;

/**
 * An interface to all sorting algorithms.
 * 
 * @author bdpanajoto
 *
 */
public interface SortingAlgorithm {

	/**
	 * Single method sort.
	 * 
	 * @param array
	 *            input to be sorted.
	 * @return a sorted representation of the array.
	 */
	public int[] sort(int[] array);

}
